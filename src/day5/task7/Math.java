package day5.task7;

public class Math {
    public int add(int n1,int n2){
        return n1 + n2;
    }
    public int subtract (int n1,int n2){
        return n1 - n2;
    }
    public int multiply (int n1,int n2){
        return n1 * n2;
    }
    public int divide(int n1,int n2){
        return n1 / n2;
    }

}
